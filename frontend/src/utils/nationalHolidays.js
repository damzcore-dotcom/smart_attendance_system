/**
 * Mendapatkan hari libur nasional untuk tanggal tertentu.
 * @param {string} dateStr Format YYYY-MM-DD
 * @returns {string|null} Nama libur atau null
 */
export const getNationalHoliday = (dateStr) => {
  return nationalHolidays[dateStr] || null;
};

/**
 * Mendapatkan semua hari libur nasional pada bulan dan tahun tertentu.
 * @param {number} year 
 * @param {number} month 1-12
 * @returns {Array<{date: string, description: string}>}
 */
export const getNationalHolidaysForMonth = (year, month) => {
  const result = [];
  const monthStr = String(month).padStart(2, '0');
  const prefix = `${year}-${monthStr}-`;

  Object.entries(nationalHolidays).forEach(([date, description]) => {
    if (date.startsWith(prefix)) {
      result.push({ date, description });
    }
  });

  return result.sort((a, b) => a.date.localeCompare(b.date));
};
